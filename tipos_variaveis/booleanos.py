# Condicionais
# se condição
# else = se não
if True:
    print("Condição verdadeira")
else:
    print("Condição falsa")

# Operadores lógicos: and e or

# And

if True and True:
    print("Bloco será executado")

if True and False:
    print("Bloco não será executado")

if False and False:
    print("Bloco não será executado")

# Or

if True or True:
    print("Bloco será executado")

if True or False:
    print("Bloco será executado")

if False or False:
    print("Bloco não será executado")
