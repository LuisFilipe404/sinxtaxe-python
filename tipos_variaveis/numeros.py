# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variável inteiro = ", type(numero_inteiro))
print("Tipo da variável real = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtração -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicação *
multiplicacao = 2 * 2
print("2 * 2 = ", multiplicacao)

# divisão /
divisao = 10 / 2
print("10 / 2 = ", divisao)

# modulo %
modulo = 10 % 3
print("10 % 3 = ", modulo)

# divisao inteira //
divisao_inteira = 10 // 3
print("10 // 3 = ", divisao_inteira)
