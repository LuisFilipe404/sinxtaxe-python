# Coleção de elementos ordenáveis e imutáveis

minha_tupla = (1, 2, 2, 3, 4)

print("Minha tupla de exemplo:", minha_tupla)
print("minha_tupla[0]:", minha_tupla[0])
print("minha_tupla[-1]:", minha_tupla[-1])  # 4

# Método count
quantidade = minha_tupla.count(2)
print("Quantidade de 2:", quantidade)

# Método index
indice = minha_tupla.index(3)
print("Índice de 3:", indice)
